package 배열;

public class 배열연습6 {

	public static void main(String[] args) {
		String[] names = {"홍길동", "박길동", "송길동"};
		for (String s : names) {
			System.out.println(s);
			
		}
	double[] eyes = {2.0, 0.5, 0.9};
	for (int i = 0; i < eyes.length; i++) {
		System.out.println(eyes[i]);
	}
	for (double d : eyes) {
		System.out.println(d);
		
	}
		
	}
	

}
