package 연산자;

public class 산술연산자 {

	public static void main(String[] args) {
		int n1 = 200;
		int n2 = 100;
	
		System.out.println("두 수의 합은: " + (n1 + n2));
		System.out.println("두 수의 차은: " + (n1 - n2));
		System.out.println("두 수의 곱은: " + (n1 * n2));
		System.out.println("두 수의 나눗셈은: " + (n1 / n2));
		System.out.println("두 수의 나머지은: " + (n1 % n2));
		
	}
	
}
